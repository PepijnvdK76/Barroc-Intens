     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $invoice = Custom_invoice::findorfail($id);
        $invoiceProduct = Custom_invoice_product::where('custom_invoice_id',$id)->first();
        $Product = Product::where('id', $invoiceProduct->product_id)->first();
        return view('web-app/invoice.show')
            ->with(['invoiceProduct'=> $invoiceProduct, 'Product' => $Product, 'invoice' => $invoice]);
    }

